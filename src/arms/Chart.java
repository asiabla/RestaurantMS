    private void addRestaurantInfo()
    {
        int RestCode=1;
        Connection conn = null;
        PreparedStatement stmt = null;
        try {
            conn = ConnectionConfiguration.getConnection();
            String sql = "SELECT * FROM restaurant WHERE Code=?";
            stmt = conn.prepareStatement(sql);
            stmt.setInt(1,RestCode);
            ResultSet rs = stmt.executeQuery();
            rs.next();
            jLabel5.setText("Restaurant " + rs.getString("Name")); //to edit
            jLabel8.setText(" Contact:  " +  rs.getString("Phone"));
            jLabel6.setText(rs.getString("Address") + ", " + rs.getString("State") + " " + rs.getInt("PostalCode"));
            rs.close();
        }
        catch(SQLException se)  { se.printStackTrace();}
        catch (Exception e) { e.printStackTrace(); }
        finally {
            try{ if(stmt!=null) conn.close(); }
            catch(SQLException se){ }
            try{ if(conn!=null) conn.close(); }
            catch(SQLException se){ se.printStackTrace(); }
        }
    }

