    private void updateTable2()
    {
        Connection conn = null;
        PreparedStatement stmt = null;
        int RowCount = jTable1.getRowCount();
        float amount=0;
        try {
            conn = ConnectionConfiguration.getConnection();
            int row_index = 0;
            int productamount;
            while(RowCount>row_index)
            {
                String product, productname, pom;
                float productprice;
                product = jTable1.getModel().getValueAt(row_index, 0).toString();
                pom = jTable1.getModel().getValueAt(row_index, 2).toString();
                productamount = Integer.valueOf(pom);
                String sql = "SELECT * FROM products WHERE IDproduct=" + product;
                stmt = conn.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();
                rs.next();
                productname = rs.getString("Name");
                productprice = rs.getFloat("Price");
                amount+=(productamount*productprice);
                rs.close();
                row_index++;
            }
        }
        catch(SQLException se)  { se.printStackTrace();}
        catch (Exception e) { e.printStackTrace(); }
        finally {
            try{ if(stmt!=null) conn.close(); }
            catch(SQLException se){ }
            try{ if(conn!=null) conn.close(); }
            catch(SQLException se){ se.printStackTrace(); }
        }
        addAmount(amount);
        jLabel26.setText(""+amount);
    }

