    private class aTask extends SwingWorker<Boolean, String> {
        @Override
        protected Boolean doInBackground() throws Exception {
            for (int i = 5; i > 0; i--) {
                publish("" + i);
                Thread.sleep(1000);
            }
            return true;
        }

        @Override
        protected void done() {
            quit();
        }

        @Override
        protected void process(List<String> txt) {
            jLabel3.setText(txt.get(txt.size() - 1));
        }
    }

