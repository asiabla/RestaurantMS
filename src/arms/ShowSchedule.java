    private void getSchedule()
    {
        String filename = "Schedule.txt";
        try
        {
            File file = new File("C:\\Users\\Joanna\\Documents\\NetBeansProjects\\ARMS\\Files\\" + filename);
            if (file.createNewFile())
            {
                jTextArea1.append("SCHEDULE FOR CURRENT WEEK HASN'T BEEN CREATED YET");
            }
            else
                {
                List<String> lines = Files.readAllLines(Paths.get("C:\\Users\\Joanna\\Documents\\NetBeansProjects\\ARMS\\Files\\" + filename));
                for (String line : lines) {
                    String newLine = System.getProperty("line.separator");
                    jTextArea1.append(line + newLine);
                }
            }
        } catch (IOException e) { e.printStackTrace(); }
    }

