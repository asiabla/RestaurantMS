    private void updateTable(){
        Connection conn = null;
        PreparedStatement stmt = null;

        try {
            conn = ConnectionConfiguration.getConnection();
            String sql = "SELECT * FROM employees";
            stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            jTable1.getColumnModel().getColumn(1).setHeaderValue("Hourly Rate ($)");
            jTable1.getColumnModel().getColumn(2).setHeaderValue("Payment ($)");
            String position, pom;
            int hours, payment;
            for(int i=0;i<jTable1.getRowCount();i++)
            {
                position = jTable1.getValueAt(i,5).toString();
                if(position.equals("manager")) {
                    jTable1.setValueAt("12", i, 1);
                    pom = jTable1.getValueAt(i,7).toString();
                    hours = Integer.valueOf(pom);
                    payment = hours * 12;
                    jTable1.setValueAt(payment, i, 2);
                }
                if(position.equals("waiter"))
                {
                    jTable1.setValueAt(" 8",i,1);
                    pom = jTable1.getValueAt(i,7).toString();
                    hours = Integer.valueOf(pom);
                    payment = hours * 8;
                    jTable1.setValueAt(payment, i, 2);
                }
                if(position.equals("cook")) {
                    jTable1.setValueAt("10", i, 1);
                    pom = jTable1.getValueAt(i,7).toString();
                    hours = Integer.valueOf(pom);
                    payment = hours * 10;
                    jTable1.setValueAt(payment, i, 2);
                }
            }
            jTable1.getTableHeader().resizeAndRepaint();
            rs.close();
        }
        catch(SQLException se)  { se.printStackTrace();}
        catch (Exception e) { e.printStackTrace(); }
        finally {
            try{ if(stmt!=null) conn.close(); }
            catch(SQLException se){ }
            try{ if(conn!=null) conn.close(); }
            catch(SQLException se){ se.printStackTrace(); }
        }
    }

